-- FetchTransportadoraByCodigo
SELECT 
CODIGO,
NOME,
NOMFANT,
NUMERO,
INSCREST,
FONE,
ENDERECO,
BAIRRO,
CIDADE,
ESTADO,
CEP
FROM TRANSPO
WHERE CODIGO = :codigo

-- FetchCondicaoByDescricao
SELECT 
CODIGO,
DESCRICAO
FROM CONDPAG
WHERE DESCRICAO = :descricao

-- FetchVendedorByNome
SELECT 
COD,
ABREV,
NOME,
FONE,
EMAIL
FROM VENDED
WHERE NOME = :nome


